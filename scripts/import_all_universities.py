def main():
    db_path = Path(__file__).parent.parent / "data" / "duckdb" / "edu_planning.db"
    conn = duckdb.connect(str(db_path))

    # 清理旧数据
    conn.execute("DELETE FROM admission_scores")
    conn.execute("DELETE FROM majors")
    conn.execute("DELETE FROM universities")

    all_unis = {}

    # 1. 添加所有 211 高校 (含 985)
    for name in UNIVERSITIES_985:
        code = CODE_MAP_211.get(name)
        all_unis[name] = {
            "name": name, "code": code, "is_985": True, "is_211": True, "is_double": True
        }

    for name in UNIVERSITIES_211_ONLY:
        code = CODE_MAP_211.get(name)
        if name not in all_unis:
            all_unis[name] = {
                "name": name, "code": code, "is_985": False, "is_211": True, "is_double": True
            }

    # 2. 添加辽宁本科高校
    for u in LIAONING_UNIS:
        name = u["name"]
        if name in all_unis:
            all_unis[name]["location"] = "辽宁" + u["location"]
            all_unis[name]["type"] = u["type"]
        else:
            all_unis[name] = {
                "name": name, "code": u["code"],
                "is_985": False, "is_211": False, "is_double": False,
                "location": "辽宁" + u["location"], "type": u["type"]
            }

    # 3. 补全
    for u in all_unis.values():
        if "location" not in u:
            u["location"] = "全国"
        if "type" not in u:
            u["type"] = "综合"

    logger.info(f"最终汇总：{len(all_unis)} 所本科高校")

    # 导入
    success = 0
    for name, u in all_unis.items():
        try:
            try:
                uni_id = int(u["code"])
            except (ValueError, TypeError):
                uni_id = abs(hash(name)) % 100000

            conn.execute("""
                INSERT INTO universities (
                    id, code, name, location, type, is_985, is_211, is_double_first_class
                ) VALUES (?, ?, ?, ?, ?, ?, ?, ?)
            """, (
                uni_id, u["code"], u["name"], u["location"], u["type"],
                u["is_985"], u["is_211"], u["is_double"]
            ))
            success += 1
        except Exception as e:
            logger.error(f"导入失败 {name}: {e}")

    conn.commit()
    conn.close()
    logger.info(f"✅ 成功导入 {success} 所本科院校")

if __name__ == "__main__":
    main()
